class Solution(object):
    def minSubArrayLen(self, s, nums):
        """
        :type s: int
        :type nums: List[int]
        :rtype: int
        """
        l = cur = 0
        ans = float('inf')
        for r, n in enumerate(nums):
            cur += n
            while cur >= s:
                ans = min(ans, r - l + 1)
                cur -= nums[l]
                l += 1
        return ans if ans != float("inf") else 0

